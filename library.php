 /**
  * Take two params and returns sum
  * 
  * @param $x the first number
  * @param $y the second number
  * @return $sum the sum
  */
  
function sum($x , $y)
{
	$sum= $x + $y;
	return $sum;
} 


/**
 * take two params and returns product
 * 
 * @param $x the first number
 * @param $y the second number
 * @return $product the product 
 * 
 */
 
 function product($x, $y)
 {
 	$product= $x * $y;
	return $product;
 }
